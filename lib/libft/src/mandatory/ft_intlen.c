#include "libft.h"

size_t	ft_intlen(int nb)
{
	int i;

	i = 0;
	if (nb < 0)
	{
		i++;
		nb *= -1;
	}
	while (nb != 0)
	{
		i++;
		nb /= 10;
	}
	return (i);
}
